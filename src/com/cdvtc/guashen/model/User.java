package com.cdvtc.guashen.model;

public class User {
    protected int userid;
    protected String userzh;
    protected String usermc;
    protected String usermm;
    protected String userdh;
    protected String userqq;

    public int getUserid() {
        return userid;
    }

    public void setUserid(int userid) {
        this.userid = userid;
    }

    public String getUserzh() {
        return userzh;
    }

    public void setUserzh(String userzh) {
        this.userzh = userzh;
    }

    public String getUsermc() {
        return usermc;
    }

    public void setUsermc(String usermc) {
        this.usermc = usermc;
    }

    public String getUsermm() {
        return usermm;
    }

    public void setUsermm(String usermm) {
        this.usermm = usermm;
    }

    public String getUserdh() {
        return userdh;
    }

    public void setUserdh(String userdh) {
        this.userdh = userdh;
    }

    public String getUserqq() {
        return userqq;
    }

    public void setUserqq(String userqq) {
        this.userqq = userqq;
    }
}
